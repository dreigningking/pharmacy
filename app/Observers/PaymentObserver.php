    /**
     * Handle the Payment "updated" event.
     *
     * @param  \App\Models\Payment  $payment
     * @return void
     */
    public function updated(Payment $payment)
    {
        if($payment->isDirty('status') && $payment->status == 'success'){
            foreach($payment->licenses as $license){
                $license->start_at = now();
                $license->warn_at = now()->addDays($license->duration_days)->subDays(2);
                $license->expire_at = now()->addDays($license->duration_days);
                $license->save();
            }
        }
    }

